export const normalizeLanguage = (language) =>
  supportedLanguages.includes(language) ? language : defaultLanguage

export const getLanguage = () => {
  if (typeof window === 'undefined') return defaultLanguage

  let settings = {}

  try {
    settings = JSON.parse(window.sessionStorage.getItem('shopSettings') || '{}')
  } catch {
    settings = {}
  }

  return normalizeLanguage(window.localStorage.getItem('language') || settings.language)
}

export const setLanguage = (language) => {
  if (typeof window === 'undefined') return defaultLanguage

  const nextLanguage = normalizeLanguage(language)
  window.localStorage.setItem('language', nextLanguage)
  window.dispatchEvent(new Event('shopmate:language-changed'))
  return nextLanguage
}

export const t = (key) => {
  const language = getLanguage()
  const resolvedKey = legacyKeyMap[key] || key
  const translated = translations[language]?.[resolvedKey]

  if (translated) return translated

  if (!missingTranslationWarnings.has(`${language}:${resolvedKey}`)) {
    missingTranslationWarnings.add(`${language}:${resolvedKey}`)
    console.warn(`Missing translation for "${resolvedKey}" in language "${language}"`)
  }

  return translations.en[resolvedKey] || resolvedKey
}
